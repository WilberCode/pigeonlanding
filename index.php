      const video = entry.target;
      // Cargar y reproducir el video
      video.src = video.dataset.src;
      video.load();
      video.play();
      } else {
      // Detener la carga y pausar la reproducción
      const video = entry.target;
      video.pause();
      video.removeAttribute('src');
      video.load();
   } 
  });
});

